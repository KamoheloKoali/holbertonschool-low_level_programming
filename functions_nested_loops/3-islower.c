/**
 * _islower -  checks if character is lowercase or uppercase
 */

int _islower(int c)
{       
        char alph = c = '0';
        char lower;
        
        for (lower = 'a';  lower < 'a'; lower++)
        {       
                if (alph == lower)
			return (1);
		else
			return (0);
	}

	return (0);
}

